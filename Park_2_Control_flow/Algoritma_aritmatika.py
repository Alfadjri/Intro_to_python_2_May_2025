x = 3
y = 2

# Penjumlahan
hasil = x + y
print(f"Hasil penjumlahan dari {x} + {y} = {hasil}")
hasil = x - y
print(f"Hasil kurang dari {x} - {y} = {hasil}")
hasil = x * y
print(f"Hasil Kali dari {x} * {y} = {hasil}")
hasil = x / y # output nya berupa float atau desimal
hasil_int = x // y
hasil_conversi = int(hasil)
print(f"Hasil bagi dari {x} / {y} = {hasil}")
print(f"Hasil bagi (integer format) dari {x} // {y} = {hasil_int}")
print(f"Hasil bagi (conversi) dari {x} / {y} = {hasil_conversi}")
# modulus
# sisa dari pembagian
hasil = x % y
print(f"Hasil sisa bagi (modulus) dari {x} % {y} = {hasil}")


# x = x + 1 atau x + 1
x = x + 1
print(f"Nilai  x = {x}")
# teknik menyingkat perubahan nilai
# incerment menambahkan nilai 
x += 1
print(f"Nilai  x = {x}")
# decerment mengkurangkan nilai
x -= 1
print(f"Nilai  x = {x}")